    private void btnBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaActionPerformed
        String busca = txtBusca.getText();

        if (busca.length() > 0) {
            boolean ehNumero = VerificaString.ehNumero(busca);

            listAlunos.clear();
            if (ehNumero) {
                listAlunos.addAll(adao.buscarQualquer(adao.getSqlBuscaMatricula(), 
                        Integer.parseInt(busca))); // matricula 
            } else {
                Aluno a = new Aluno();
                a.setNome(busca);
                listAlunos.addAll(adao.buscar(a)); // parte do nome
                listAlunos.addAll(adao.buscarQualquer(adao.getSqlBuscaLogin(), busca)); // login
            }
            int linhas = listAlunos.size() - 1;
            if (linhas >= 0) {
                tblAlunos.setRowSelectionInterval(0, 0);
                tblAlunos.scrollRectToVisible(tblAlunos.getCellRect(0, 0, true));
            } else {
                JOptionPane.showConfirmDialog(null, "Nenhuma correspondência encontrada.",
                "Busca sem resultado", JOptionPane.DEFAULT_OPTION);
                try {
                    atualizaTabela();
                } catch (SQLException ex) {
                    Logger.getLogger(LivroView.class.getName()).log(Level.SEVERE, null, ex);
                }
            }     
        } else {
            try {
                atualizaTabela();
            } catch (SQLException ex) {
                Logger.getLogger(LivroView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnBuscaActionPerformed

