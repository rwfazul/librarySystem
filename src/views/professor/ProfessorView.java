    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (validaCampos()) {
            int linhaSelecionada = tblProfessores.getSelectedRow();
            Professor p = listProfessores.get(linhaSelecionada);
            if (p.getCodigo() != null) {
                pdao.alterar(p);
            } else {
                pdao.inserir(p);
            }
            trataEdicao(false);
            try {
                atualizaTabela();
            } catch (SQLException ex) {
                Logger.getLogger(LivroView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tblProfessores.getSelectedRow();
        if (linhaSelecionada >= 0) {
            if (JOptionPane.showOptionDialog(null, "Tem certeza que deseja excluir o professor?",
                    "Confirmar exclusão", JOptionPane.YES_NO_OPTION, 
                    JOptionPane.QUESTION_MESSAGE,
                    null, new String[]{"Sim", "Não"}, "Sim") == 0) {
                Professor p = listProfessores.get(linhaSelecionada);
                if (p.getValorMulta() > 0) {
                    JOptionPane.showConfirmDialog(null, "Professor possui multa pendente.\n"
                            + "Ação necessária: Quitar a multa.",
                            "Exclusão impossível", JOptionPane.DEFAULT_OPTION);
                    return;
                }
                if (p.getNrLivros() > 0) {
                    JOptionPane.showConfirmDialog(null, "Professor possui livros emprestados.\n"
                            + "Ação necessária: Devolver livros.",
                            "Exclusão impossível", JOptionPane.DEFAULT_OPTION);
                    return;
                }
                pdao.excluir(p);
                try {
                    atualizaTabela();
                    if (listProfessores.size() - 1 >= 0) {
                        tblProfessores.setRowSelectionInterval(0, 0);
                        tblProfessores.scrollRectToVisible(tblProfessores.getCellRect(0, 0, true));
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(LivroView.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showConfirmDialog(null, "Não há professores para exclusão.",
                    "Lista de professores vazia", JOptionPane.DEFAULT_OPTION);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

