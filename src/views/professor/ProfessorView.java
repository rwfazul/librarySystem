    private void btnBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaActionPerformed
        String busca = txtBusca.getText();

        if (busca.length() > 0) {
            boolean ehNumero = VerificaString.ehNumero(busca);

            listProfessores.clear();
            if (ehNumero) {
                listProfessores.addAll(pdao.buscarQualquer(pdao.getSqlBuscaMatricula(), 
                        Integer.parseInt(busca))); // matricula 
            } else {
                Professor p = new Professor();
                p.setNome(busca);
                listProfessores.addAll(pdao.buscar(p)); // parte do nome
                listProfessores.addAll(pdao.buscarQualquer(pdao.getSqlBuscaLogin(), busca)); // login
            }
            int linhas = listProfessores.size() - 1;
            if (linhas >= 0) {
                tblProfessores.setRowSelectionInterval(0, 0);
                tblProfessores.scrollRectToVisible(tblProfessores.getCellRect(0, 0, true));
            } else {
                JOptionPane.showConfirmDialog(null, "Nenhuma correspondência encontrada.",
                "Busca sem resultado", JOptionPane.DEFAULT_OPTION);
                try {
                    atualizaTabela();
                } catch (SQLException ex) {
                    Logger.getLogger(LivroView.class.getName()).log(Level.SEVERE, null, ex);
                }
            }     
        } else {
            try {
                atualizaTabela();
            } catch (SQLException ex) {
                Logger.getLogger(LivroView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnBuscaActionPerformed

    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        listProfessores.add((Professor) new Professor());
        int linhas = listProfessores.size() - 1;
        tblProfessores.setRowSelectionInterval(linhas, linhas);
        trataEdicao(true);
        txtMatricula.requestFocus();
    }//GEN-LAST:event_btnNovoActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tblProfessores.getSelectedRow() >= 0) {
            trataEdicao(true);
            txtMatricula.requestFocus();
        } else {
            JOptionPane.showConfirmDialog(null, 
                    "Algum professor deve ser selecionado para edição.",
                    "Lista de professores vazia", 
                    JOptionPane.DEFAULT_OPTION);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

