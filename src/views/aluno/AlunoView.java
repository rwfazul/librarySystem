    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (validaCampos()) {
            int linhaSelecionada = tblAlunos.getSelectedRow();
            Aluno a = listAlunos.get(linhaSelecionada);            
            if (a.getCodigo() != null) {
                adao.alterar(a);
            } else {
                adao.inserir(a);
            }
            trataEdicao(false);
            try {
                atualizaTabela();
            } catch (SQLException ex) {
                Logger.getLogger(LivroView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tblAlunos.getSelectedRow();
        if (linhaSelecionada >= 0) {
            if (JOptionPane.showOptionDialog(null, "Tem certeza que deseja excluir o aluno?",
                    "Confirmar exclusão", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,
                    null, new String[]{"Sim", "Não"}, "Sim") == 0) {
                Aluno a = listAlunos.get(linhaSelecionada);
                if (a.getValorMulta() > 0) {
                    JOptionPane.showConfirmDialog(null, "Aluno possui multa pendente.\n"
                            + "Ação necessária: Quitar a multa.",
                            "Exclusão impossível", JOptionPane.DEFAULT_OPTION);
                    return;
                }
                if (a.getNrLivros() > 0) {
                    JOptionPane.showConfirmDialog(null, "Aluno possui livros emprestados.\n"
                            + "Ação necessária: Devolver livros.",
                            "Exclusão impossível", JOptionPane.DEFAULT_OPTION);
                    return;
                }
                adao.excluir(a);
                try {
                    atualizaTabela();
                    if (listAlunos.size() - 1 >= 0) {
                        tblAlunos.setRowSelectionInterval(0, 0);
                        tblAlunos.scrollRectToVisible(tblAlunos.getCellRect(0, 0, true));
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(LivroView.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showConfirmDialog(null, "Não há alunos para exclusão.",
                    "Lista de alunos vazia", JOptionPane.DEFAULT_OPTION);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

