    public void atualizaTabela() throws SQLException {
        listProfessores.clear();
        listProfessores.addAll(pdao.buscarTodos());
        int linha = listProfessores.size() - 1;
        if (linha >= 0) {
            tblProfessores.setRowSelectionInterval(linha, linha);
            tblProfessores.scrollRectToVisible(
                    tblProfessores.getCellRect(linha, linha, true));
            barra_navegacao(true);
        } else {
            limpa_campos();
            barra_navegacao(false);
        }
        txtBusca.setText("");
    }

    public void trataEdicao(boolean editando) {
        // botoes ativados se estivar editando 
        btnCancelar.setEnabled(editando);
        btnSalvar.setEnabled(editando);
        // botoes desativados se estivar editando 
        btnEditar.setEnabled(!editando);
        btnExcluir.setEnabled(!editando);
        btnNovo.setEnabled(!editando);
        btnFechar.setEnabled(!editando);
        btnPrimeiro.setEnabled(!editando);
        btnProximo.setEnabled(!editando);
        btnAnterior.setEnabled(!editando);
        btnUltimo.setEnabled(!editando);
        btnBusca.setEnabled(!editando);
        // campos ativados se estivar editando 
        txtMatricula.setEditable(editando);
        txtNome.setEditable(editando);
        txtMulta.setEditable(editando);
        txtLivros.setEditable(editando);
        txtLogin.setEditable(editando);
        // desativado
        txtBusca.setEditable(!editando);
        // tabela
        tblProfessores.setEnabled(!editando);
    }

    public boolean validaCampos() {
        if (txtVazio(txtLogin, "o login")) {
            return false;
        }
        
        if (txtVazio(txtMatricula, "a matricula")) {
            return false;
        }

        if (!VerificaString.ehNumero(txtMatricula.getText())) {
            JOptionPane.showMessageDialog(null, "A matrícula deve conter apenas números");
            txtMatricula.requestFocus();
            return false;
        }

        if (txtVazio(txtNome, "o nome")) {
            return false;
        }

        if (txtMulta.getText().isEmpty()) {
            txtMulta.setText("0");
        }

        if (txtLivros.getText().isEmpty()) {
            txtLivros.setText("0");
        }

        if (!VerificaString.ehNumero(txtLivros.getText())) {
            JOptionPane.showMessageDialog(null, "O número de livros deve conter apenas números");
            txtLivros.requestFocus();
            return false;
        }

        return true;
    }

    public boolean txtVazio(JTextField txt, String msg) {
        if (txt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe " + msg + " do professor");
            txt.requestFocus();
            return true;
        }

        return false;
    }

    public void limpa_campos() {
        txtCodigo.setText("");
        txtMatricula.setText("");
        txtNome.setText("");
        txtLivros.setText("");
        txtMulta.setText("");
    }
    
    public void barra_navegacao(Boolean b) {
        btnPrimeiro.setEnabled(b);
        btnAnterior.setEnabled(b);
        btnProximo.setEnabled(b);
        btnUltimo.setEnabled(b);
    }   

